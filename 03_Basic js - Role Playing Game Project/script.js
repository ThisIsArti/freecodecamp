function buyHealth() {
  if (gold >= 10) {
    //checks if player has enough gold to buy health
    gold -= 10;
    health += 10;
    goldText.innerText = gold;
    healthText.innerText = health;
  } else {
    text.innerText = "You do not have enough gold to buy health.";
  }
}

function buyWeapon() {
  if (currentWeaponIndex < weapons.length - 1) {
    //if player has best weapon, they cannot buy another. array indexing starts at 0, so index of last element in array is 1 less than length of array
    if (gold >= 30) {
      //checks if player has enough gold to buy weapon
      gold -= 30;
      currentWeaponIndex++; //compound assignment adds 1, to allow user to buy next weapon in weapons array
      goldText.innerText = gold;
      let newWeapon = weapons[currentWeaponIndex].name; //tells player which new weapon they have bought
      text.innerText = "You now have a " + newWeapon + ".";
      inventory.push(newWeapon);
      text.innerText += " In your inventory you have: " + inventory;
    } else {
      text.innerText = "You do not have enough gold to buy a weapon.";
    }
  } else {
    text.innerText = "You already have the most powerful weapon!";
    button2.innerText = "Sell weapon for 15 gold";
    button2.onclick = sellWeapon;
  }
}

function sellWeapon() {
  if (inventory.length > 1) {
    //stops player selling only weapon. checks length of inventory array is > 1
    gold += 15;
    goldText.innerText = gold;
    let currentWeapon = inventory.shift();
    text.innerText = "You sold a " + currentWeapon + ".";
    text.innerText += " In your inventory you have: " + inventory;
  } else {
    text.innerText = "Don't sell your only weapon!";
  }
}

//FIGHT FUNCTIONS

function fightSlime() {
  fighting = 0;
  goFight();
}

function fightBeast() {
  fighting = 1;
  goFight();
}

function fightDragon() {
  fighting = 2;
  goFight();
}

function goFight() {
  update(locations[3]);
  monsterHealth = monsters[fighting].health;
  monsterStats.style.display = "block"; //.style used to access inline style of element, .display property used to set visibility of element
  monsterName.innerText = monsters[fighting].name;
  monsterHealthText.innerText = monsterHealth;
}

function attack() {
  text.innerText = "The " + monsters[fighting].name + " attacks.";
  text.innerText +=
    " You attack it with your " + weapons[currentWeaponIndex].name + ".";
  health -= getMonsterAttackValue(monsters[fighting].level); //sets health equal to health minus return value of getMonsterAttackValue function, then passes level of monster as argument
  if (isMonsterHit()) {
    monsterHealth -=
      weapons[currentWeaponIndex].power + Math.floor(Math.random() * xp) + 1;
  } else {
    text.innerText += " You miss.";
  }
  healthText.innerText = health;
  monsterHealthText.innerText = monsterHealth;
  if (health <= 0) {
    lose();
  } else if (monsterHealth <= 0) {
    if (fighting === 2) {
      winGame();
    } else {
      defeatMonster();
    }
  }
  if (Math.random() <= 0.1 && inventory.length !== 1) {
    //creates chance that player's weapon breaks on every attack
    //checks if player has only 1 weapon. if so, does not run function and break
    text.innerText += " Your " + inventory.pop() + " breaks."; // removes last item in array and returns it so it appears in string
    currentWeaponIndex--; //decrement operator decreased currentWeaponIndex by 1
  }
}

function getMonsterAttackValue(level) {
  const hit = level * 5 - Math.floor(Math.random() * xp); //sets monster attack to 5 times their level, minus random number between 0 and player's xp
  console.log(hit);
  return hit > 0 ? hit : 0; //stops getMonsterAttackValue function returning a negative number if xp is high enough
}

function isMonsterHit() {
  return Math.random() > 0.2 || health < 20; //player should hit if either Math.random() > .2 OR health less than 20
}

function dodge() {
  text.innerText = "You dodge the attack from the " + monsters[fighting].name;
}

function defeatMonster() {
  gold += Math.floor(monsters[fighting].level * 6.7);
  xp += monsters[fighting].level;
  goldText.innerText = gold;
  xpText.innerText = xp;
  update(locations[4]);
}

function lose() {
  update(locations[5]);
}

function winGame() {
  update(locations[6]);
}

function restart() {
  xp = 0;
  health = 100;
  gold = 50;
  currentWeaponIndex = 0;
  inventory = ["stick"];
  goldText.innerText = gold;
  healthText.innerText = health;
  xpText.innerText = xp;
  goTown();
}

