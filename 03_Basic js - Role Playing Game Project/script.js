//clean-up of location function
function update(location) {
  monsterStats.style.display = "none"; //monster stat box does not display after monster is defeated
  button1.innerText = location["button text"][0]; //innerText controls text that appears in html element.
  button2.innerText = location["button text"][1];
  button3.innerText = location["button text"][2];
  button1.onclick = location["button functions"][0];
  button2.onclick = location["button functions"][1];
  button3.onclick = location["button functions"][2];
  text.innerHTML = location.text; //innerHTML allows you to access or modify the content inside HTML element, using js
}

